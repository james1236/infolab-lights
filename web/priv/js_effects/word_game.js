    //The point of interaction for the class, this is called by the user
    guess(word) {
        let response;

        if (this.gameState == this.GAME_LOST) {
            return "FAIL: Game already lost";
        }

        if (this.gameState == this.GAME_WON) {
            return "FAIL: Game already won";
        }

        if (this.dailyAnswers.indexOf(word.toLowerCase()) == -1) {
            return "FAIL: Word not allowed";
        }

        for (let guess of this.guesses) {
            if (guess.word == word) {
                return "FAIL: Word already guessed";
            }
        }

        this.#addGuess(word);

        if (word == this.answer) {
            return "SUCCESS: Game won";
        }

        return "SUCCESS: Guess accepted";
    }

    // Repeatedly try to automatically guess words until the game is won or lost waiting between guesses
    #tryAutoGuess(instant = false) {
        clearInterval(this.autoGuessInterval);
        this.autoGuessInterval = setTimeout(() => {
            if (this.gameState == this.GAME_PLAYING) {
                this.#autoGuess();
                clearInterval(this.autoGuessInterval);
                this.autoGuessInterval = this.#tryAutoGuess();
            }
        }, (instant) ? 1 : (Math.random() * 3000) + 4000);
    }

    // Score a guess, add it to guesses and update game states
    #addGuess(word) {
        let guess = this.#scoreGuess(word);
        console.log("Guess: " + word);
        this.guesses[this.tryCount] = guess;

        this.tryCount++;

        if (this.gameState == this.GAME_PLAYING) {
            if (this.tryCount >= this.triesAllowed) {
                this.gameState = this.GAME_LOST;

                setTimeout(() => {this.startGame();}, 8000); //Schedule a new game to start 
                setTimeout(() => {this.reverseAnimation = true;}, 7000);
            }
        }

        if (word == this.answer) {
            this.gameState = this.GAME_WON;

            setTimeout(() => {this.startGame();}, 8000); //Schedule a new game to start
            setTimeout(() => {this.reverseAnimation = true;}, 7000);
        }
    }

    // Create a guess object for a word
    #scoreGuess(word, answer = this.answer) {
        let tempAnswer = answer.split("");

        let guess = {
            word: word,
            colors: [this.GRAY, this.GRAY, this.GRAY, this.GRAY, this.GRAY],
            animationPercentages: [0, 0, 0, 0, 0]
        };

        //Do all greens first 
        for (let i = 0; i < 5; i++) {
            if (word[i] == tempAnswer[i]) {
                guess.colors[i] = this.GREEN;
                tempAnswer[i] = "#"; //Remove the character from the answer so it won't be seen as yellow
            }
        }

        for (let i = 0; i < 5; i++) {
            if (tempAnswer[i] == "#") {
                continue; //Skip this character if it was already used as a green
            }

            let position = tempAnswer.indexOf(word[i]);
            if (position != -1) {
                guess.colors[i] = this.YELLOW;
                tempAnswer[position] = "#"; //Remove that character from the answer so it won't be seen as yellow again
            }
        }

        return guess;
    }

    //Flip letter animation 
    #revealGuess() {
        for (let guess of this.guesses) {
            if (guess.word != "#####") {

                if (this.reverseAnimation) {
                    for (let i = 0; i < 5; i++) {
                        guess.animationPercentages[i] = Math.max(0, guess.animationPercentages[i] - 0.1);
                    }

                } else {
                    // One at a time
                    let i = 0;
                    while (guess.animationPercentages[i] == 1) {
                        i++;
                    }
                    if (i > guess.animationPercentages.length) {
                        return;
                    }

                    guess.animationPercentages[i] = Math.min(guess.animationPercentages[i] + 0.1, 1);
                }
            }
        }
    }

    #autoGuess() {
        // Guess randomly if no guesses have been made
        if (this.tryCount == 0) {
            this.#addGuess(this.dailyAnswers[Math.floor(Math.random() * this.dailyAnswers.length)]);
            return;
        }

        let letterBlacklist = {};
        let letterWhitelist = {};

        for (let i = 0; i < this.tryCount; i++) {
            let guess = this.guesses[i];

            for (let j = 0; j < guess.word.length; j++) {
                // Find the list of letters that can't be in the answer
                if (guess.colors[j] == this.GRAY) {
                    // Only blacklist if it's really not in the word (not just gray because it's a double letter with a green already)
                    if (this.answer.indexOf(guess.word[j]) == -1) {
                        // Mark letter as blacklisted in all positions
                        letterBlacklist[guess.word[j]] = [...Array(this.answer.length).keys()];
                    }
                }

                if (guess.colors[j] == this.YELLOW) {
                    // Mark letter as blacklisted in the current position
                    if (letterBlacklist[guess.word[j]] == undefined) {
                        letterBlacklist[guess.word[j]] = [j];
                    } else {
                        if (letterBlacklist[guess.word[j]].indexOf(j) == -1) {
                            letterBlacklist[guess.word[j]].push(j);
                        }
                    }

                    // Mark letter as whitelisted in all other positions
                    letterWhitelist[guess.word[j]] = [...Array(this.answer.length).keys()];
                    letterWhitelist[guess.word[j]].splice(j, 1);
                }

                // Find the list of letters that must be in the answer
                if (guess.colors[j] == this.GREEN) {
                    letterWhitelist[guess.word[j]] = [j];
                }
            }
        }

        //Find the list of remaining possibilities given the blacklisted and whitelisted letters
        let possibleWords = [];
        for (let word of this.dailyAnswers) {
            let possible = true;
            for (let letter in letterBlacklist) {
                // If the word contains a letter in a blacklisted position, it is not possible
                for (let position of letterBlacklist[letter]) {
                    if (word[position] == letter) {
                        possible = false;
                        break;
                    }
                }
            }

            // For every word, every letter of whitelist, if the word doesn't contain the letter, in any of the listed positions, it is not possible
            for (let letter in letterWhitelist) {
                let any = false;
                for (let position of letterWhitelist[letter]) {
                    if (word[position] == letter) {
                        any = true;
                        break;
                    }
                }

                if (!any) {
                    possible = false;
                    break;
                }
            }

            if (possible) {
                possibleWords.push(word);
            }
        }

        //Find the number of answers that contain each letter that isn't blacklisted
        let letterCounts = {};
        for (let word of possibleWords) {
            for (let letter of word) {
                if (letterBlacklist[letter] == undefined) {
                    if (letterCounts[letter] == undefined) {
                        letterCounts[letter] = 1;
                    } else {
                        letterCounts[letter]++;
                    }
                }
            }
        }

        let letterTotal = 0;
        for (let letter in letterCounts) {
            letterTotal += letterCounts[letter];
        }

        //Find the word that divides the possible words in half the most evenly
        console.log("Possible words: " + possibleWords.length);
        let bestWord = possibleWords[0];
        let bestScore = 999999999999;
        for (let word of possibleWords) {

            let alreadyGuessed = false;
            for (let guess of this.guesses) {
                if (guess.word == word) {
                    alreadyGuessed = true;
                    break;
                }
            }
            if (alreadyGuessed) {
                continue;
            }

            let score = 0;
            for (let letter of word) {
                score += letterCounts[letter];
            }
            if (Math.abs(score - (letterTotal / 2)) < bestScore) {
                bestWord = word;
                bestScore = score;
            }
        }

        //console.log(letterBlacklist);
        //console.log(letterWhitelist);

        console.assert(bestWord != undefined, "No best word found");
        this.#addGuess(bestWord);
    }

